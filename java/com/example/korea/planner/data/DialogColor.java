public class DialogColor {
    private int color;
    private boolean checked;

    public void setChecked(boolean checked) {
        this.checked = checked;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public boolean getChecked() {
        return checked;
    }

    public int getColor() {
        return color;
    }
}
